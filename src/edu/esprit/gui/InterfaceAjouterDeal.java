            Deal deal =new Deal();
          
            deal.setTitre(txttitre.getText());                                                                              
            deal.setDetails(txtdetail.getText());
            deal.setPrix(Integer.parseInt(txtprix.getText()));
            deal.setPrix_promo( Integer.parseInt(txtprixpromo.getText()));
